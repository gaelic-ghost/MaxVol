#!/usr/bin/env python3
"""Generate deterministic benchmark fixtures shared by Swift and Python harnesses."""

from __future__ import annotations

import argparse
import json
from pathlib import Path


def next_double(state: int) -> tuple[int, float]:
    state = (state * 6_364_136_223_846_793_005 + 1_442_695_040_888_963_407) & ((1 << 64) - 1)
    scaled = (state >> 11) / float((1 << 53) - 1)
    return state, scaled * 2.0 - 1.0


def gaussian_like(rows: int, columns: int, seed: int, diagonal_boost: float) -> list[float]:
    state = seed
    values: list[float] = []
    for row in range(rows):
        for column in range(columns):
            state, first = next_double(state)
            state, second = next_double(state)
            value = 0.5 * first + 0.5 * second
            if row == column:
                value += diagonal_boost
            values.append(value)
    return values


def dot(left: list[float], right: list[float]) -> float:
    return sum(a * b for a, b in zip(left, right))


def orthonormal(rows: int, columns: int, seed: int) -> list[float]:
    state = seed
    vectors: list[list[float]] = []
    for column in range(columns):
        vector: list[float] = []
        for row in range(rows):
            state, value = next_double(state)
            vector.append(value + (1.0 if row == column else 0.0))
        vectors.append(vector)

    for column in range(columns):
        for prior in range(column):
            projection = dot(vectors[column], vectors[prior])
            for row in range(rows):
                vectors[column][row] -= projection * vectors[prior][row]

        norm = dot(vectors[column], vectors[column]) ** 0.5
        for row in range(rows):
            vectors[column][row] /= norm

    return [vectors[column][row] for row in range(rows) for column in range(columns)]


def fixture(identifier: str, rows: int, columns: int, values: list[float], description: str) -> dict:
    return {
        "id": identifier,
        "description": description,
        "rows": rows,
        "columns": columns,
        "rowMajorValues": values,
    }


def build_fixtures() -> dict:
    return {
        "version": 1,
        "source": "Generated by scripts/generate_benchmark_fixtures.py.",
        "fixtures": [
            fixture(
                "square-swap-3x2",
                3,
                2,
                [
                    0.25, 0.0,
                    0.5, 1.0,
                    0.5, 1.5,
                ],
                "Small fixture that performs one square MaxVol row swap.",
            ),
            fixture(
                "rect-append-5x2",
                5,
                2,
                [
                    1.0, 0.0,
                    0.0, 1.0,
                    1.2, 0.2,
                    0.1, 1.3,
                    0.8, 0.8,
                ],
                "Small fixture that appends one RectMaxVol row.",
            ),
            fixture(
                "gaussian-64x8",
                64,
                8,
                gaussian_like(64, 8, 0x6408_2026, 1.0),
                "Medium full-rank Gaussian-like tall matrix with a diagonal boost.",
            ),
            fixture(
                "orthonormal-96x12",
                96,
                12,
                orthonormal(96, 12, 0x9612_2026),
                "Medium tall matrix with deterministic orthonormal columns.",
            ),
            fixture(
                "gaussian-192x16",
                192,
                16,
                gaussian_like(192, 16, 0x1921_62026, 1.0),
                "Larger full-rank Gaussian-like tall matrix for timing and profiling.",
            ),
        ],
    }


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--output",
        default="Sources/MaxVolBenchmark/Resources/fixtures.json",
        help="Fixture JSON path to write.",
    )
    args = parser.parse_args()

    output = Path(args.output)
    output.parent.mkdir(parents=True, exist_ok=True)
    output.write_text(json.dumps(build_fixtures(), indent=2, sort_keys=True) + "\n", encoding="utf-8")


if __name__ == "__main__":
    main()
